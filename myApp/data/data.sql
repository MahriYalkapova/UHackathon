DROP TABLE IF EXISTS Fetchly;
CREATE DATABASE Fetchly;

DROP TABLE IF EXISTS ITEMS;
DROP TABLE IF EXISTS USERS;

CREATE TABLE ITEMS(
    itemID INT PRIMARY KEY,
    name TEXT,
    image_link TEXT
);

CREATE TABLE USER (
    userID INT PRIMARY KEY,
    firstN VARCHAR(255) NOT NULL,
    lastN VARCHAR(255),
    email VARCHAR(255),
    phoneNum VARCHAR(32)
)